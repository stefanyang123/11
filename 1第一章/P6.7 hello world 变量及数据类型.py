# 变量的命名规则
# 必须以字母a-z A-Z 或下划线开头
# 其他字符可以是字母，数字或_
# 变量区分大小写
# python关键字不能用作变量名
# 不能以数字开头！
# 变量名支持中文
Name='刘德华'
name='杨博文'
_age=50
中文=1
print(Name,_age)
print(Name,name)
print(中文)
# 命名规范
# 见名知意：尽量使用有语义的单词命名。如password用作密码，username用户名
# 小驼峰式命名法：第一个单词首字母小写其他单词首字母大写 如userName
# 大驼峰式命名法：全部单词首字母大写 如UserName
# 下划线命名法：每个单词用下划线连接  如user_name

