# 1.什么是变量：
# 变量就是一个盒子，里面可以装进各种东西，和初中代数的变量是一致的
# 方程式y=x*3  x就是变量 当x=2时 结果y=6
# 变量不仅仅是数字，还可以是其他任意类型 ，变量=存储的数据

# 2.变量定义：
# 变量是一段有名字的连续的存储的空间，我们可以通过定义变量来申请并命名这样的存储空间，并通过变量的名字来使用这段存储空间
# 变量是程序中临时存放数据的场所
# python中赋值变量不需要指定数据类型，给变量赋值什么数据类型，这个变量就是什么类型

# 3.变量如何来定义和使用:
# 定义规则 :  变量名=数据
# a就是变量名 对于一个盒子 里面数据就是10
a=10
#使用变量
print(a)
# 当你给a赋值10时  又给啊赋值20 则此时a为20
